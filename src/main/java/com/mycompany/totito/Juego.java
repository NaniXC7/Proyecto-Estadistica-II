    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int endGame = JOptionPane.showConfirmDialog(null, "Desea terminar el juego");
        if(endGame == 0){
        
            DatosEstadisticos totalDatos = new DatosEstadisticos();
            //winX = Integer.parseInt(Lbl_winX.getText());
            totalDatos.setWinX(contadorX);
            totalDatos.setWinO(contadorO);
            totalDatos.setDraws(contadorDraw);
            totalDatos.setTotalMatch(contadorMatches);
            totalDatos.setVisible(true);
            dispose();
        
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    public void Presionar(int Casilla){
    
        if(this.lbs[Casilla-1].getText().equals("") && !this.confirmDraw){
        
            this.lbs[Casilla-1].setText(turn);
            CambiarTurno();
            ComprobarGanador();
        
        }
        
    }
    
    public void CambiarTurno(){
    
        if(turn.equals("X")){
        
            turn = "O";
        
        }else{
        
            turn = "X";
        
        }
        
        Lbs_Estado.setText("Turno de: " + turn);
    
    }
    
    public void ComprobarGanador(){
        for (int i = 0; i < this.win.length; i++) {
                
                if(lbs[this.win[i][0]-1].getText().equals("X") &&
                    lbs[this.win[i][1]-1].getText().equals("X") &&
                    lbs[this.win[i][2]-1].getText().equals("X")){
                    
                    lbs[this.win[i][0]-1].setBackground(Color.GREEN);
                    lbs[this.win[i][1]-1].setBackground(Color.GREEN);
                    lbs[this.win[i][2]-1].setBackground(Color.GREEN);
                    Lbs_Estado.setText("El ganador es X!");
                    this.confirmDraw = true;
                    this.contadorX++;
                    this.contadorMatches++;
                    Lbl_winX.setText(Integer.toString(contadorX));
                
                }
                if(lbs[this.win[i][0]-1].getText().equals("O") &&
                            lbs[this.win[i][1]-1].getText().equals("O") &&
                            lbs[this.win[i][2]-1].getText().equals("O")){
                
                    lbs[this.win[i][0]-1].setBackground(Color.GREEN);
                    lbs[this.win[i][1]-1].setBackground(Color.GREEN);
                    lbs[this.win[i][2]-1].setBackground(Color.GREEN);
                    Lbs_Estado.setText("El ganador es O!");
                    this.confirmDraw = true;
                    this.contadorO++;
                    this.contadorMatches++;
                    Lbl_winO.setText(Integer.toString(contadorO));
                    
                
                }
                
                
        }
        
        turns++;
        
        if(turns == 9 && !this.confirmDraw){
                
                 Lbs_Estado.setText("Es un empate! :0");
                 Lbl_1.setBackground(Color.YELLOW);
                 Lbl_2.setBackground(Color.YELLOW);
                 Lbl_3.setBackground(Color.YELLOW);
                 Lbl_4.setBackground(Color.YELLOW);
                 Lbl_5.setBackground(Color.YELLOW);
                 Lbl_6.setBackground(Color.YELLOW);
                 Lbl_7.setBackground(Color.YELLOW);
                 Lbl_8.setBackground(Color.YELLOW);
                 Lbl_9.setBackground(Color.YELLOW);
                 this.contadorDraw++;
                 this.contadorMatches++;
                 Lbl_Draw.setText(Integer.toString(contadorDraw));
                    
        }
        

        
    
    }
    public void borrar(){
    
    turns = 0;
        Lbs_Estado.setText("");
        for (int i = 0; i < lbs.length; i++) {
            lbs[i].setText("");
            lbs[i].setBackground(Color.white);
        }
        
        turn = nextTurn;
        
        if(nextTurn.equals("O")){
        
            nextTurn = "X";
        
        }else{
        
            nextTurn = "O";
        
        }
        Lbs_Estado.setText("Turno de: " + turn);
        this.confirmDraw = false;
    
    }
    
    
    
    
    
